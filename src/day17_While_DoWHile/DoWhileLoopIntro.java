public class DoWhileLoopIntro {
    public static void main(String[] args) {
        boolean a = false;

        while (a){
            System.out.println("wooden spoon");
        }
        System.out.println("+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++");
        do{
            System.out.println("wooden spoon");
        }while(a);
        System.out.println("+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++");

        for(int i =0;a;){
            System.out.println("wooden spoon");
        }



    }
}
