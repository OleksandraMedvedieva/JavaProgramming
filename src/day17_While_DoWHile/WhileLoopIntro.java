package day17_While_DoWHile;

public class WhileLoopIntro {
    public static void main(String[] args) {
        if(true){
            System.out.println("wooden spoon"); // 1 time
        }

        System.out.println("=========================================================");

        while(true){
            System.out.println("wooden spoon"); // executed while the condition is true
        }
    }
}
